'''ProgressiveTax'''
def main(money=int(input())):
    '''ProgressiveTax'''
    if money <= 150000:
        print("0")
    elif money > 150000 and money <= 300000:
        print(int((money-150000)*(5/100)))
    elif money > 300000 and money <= 500000:
        print(int((money-300000)*(10/100)+(money-300000-150000)*(5/100)))
    elif money > 500000 and money <= 750000:
        print(int((money-500000)*(15/100)+(money-500000-300000)*(10/100)\
            +(money-500000-300000-150000)*(5/100)))
    elif money > 750000 and money <= 1000000:
        print(int((money-750000)*(20/100)+(money-750000-500000)*(15/100)\
            +(money-750000-500000-300000)*(10/100)+(money-750000-500000-300000-150000)*(5/100)))
    elif money > 1000000 and money <= 2000000:
        print(int((money-1000000)*(25/100)+(money-1000000-750000)*(20/100)\
            +(money-1000000-750000-500000)*(15/100)+(money-1000000-750000-500000-300000)*(10/100)\
                +(money-1000000-750000-500000-300000-150000)*(5/100)))
    elif money > 2000000 and money <= 4000000:
        print(int((money-2000000)*(30/100)+(money-2000000-1000000)*(25/100)\
            +(money-2000000-1000000-750000)*(20/100)+(money-2000000-1000000-750000-500000)*(15/100)\
                +(money-2000000-1000000-750000-500000-300000)*(10/100)\
                    +(money-2000000-1000000-750000-500000-300000-150000)*(5/100)))
    else:
        print(int((money-4000000)*(35/100)+(money-4000000-2000000)*(30/100)+(money-4000000-2000000-1000000)*(25/100)\
            +(money-4000000-2000000-1000000-750000)*(20/100)+(money-4000000-2000000-1000000-750000-500000)*(15/100)\
                +(money-4000000-2000000-1000000-750000-500000-300000)*(10/100)\
                    +(money-4000000-2000000-1000000-750000-500000-300000-150000)*(5/100)))
main()
